public class tipedata {
    public static void main(String[] args) {
        char golongandarah = 'A';
        byte jarak = (byte) 220;
        short jumlahpenduduk = 2000;
        float suhu = 70.50f;
        double berat = 0.54545454;
        long saldo = 250000000;
        int angkadesimal = 0x10;
        
        // tampilan
        System.out.println("gologan darah" + golongandarah);
        System.out.println("gologan darah\t\t" + golongandarah);
        System.out.println();
        System.out.println("jarak" + jarak);
        System.out.println("jarak\t\t" + jarak);
        System.out.println();
        System.out.println("Suhu" + suhu);
        System.out.println("Suhu\t\t" + suhu);
        System.out.println();
        System.out.println("beraat " + (float) berat);
        System.out.println("beraat \t\t" + (float) berat);
        System.out.println();
        System.out.println("saldo" + saldo);
        System.out.println("saldo\t\t" + saldo);
        System.out.println();
        System.out.println("desimal" + angkadesimal);
        System.out.println("desimal\t\t" + angkadesimal);
    }
}
